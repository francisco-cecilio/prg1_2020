package ch.teko.prg.tag02.work;

public class XY {
    //Variabeln
    public int x;
    public int y;

    //Konstruktoren
    public XY(int x, int y){
        this.x = x;
        this.y = y;
    }

    public XY(){
        this.x = 0;
        this.y = 0;
    }
    /*

    */

    //Methoden
    public int getX(){
        return x;
    }

    public int getY(){
        return y;

    }

}
